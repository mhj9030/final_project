package com.final_project.help;

public class ClaimReply {
	private int replyNum;
	private String replyAnswer, date, mId, mName;
	private int claimNum;
	
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getReplyAnswer() {
		return replyAnswer;
	}
	public void setReplyAnswer(String replyAnswer) {
		this.replyAnswer = replyAnswer;
	}
	public int getReplyNum() {
		return replyNum;
	}
	public void setReplyNum(int replyNum) {
		this.replyNum = replyNum;
	}
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getmId() {
		return mId;
		
	}
	public void setmId(String mId) {
		this.mId = mId;
	}
	public int getClaimNum() {
		return claimNum;
	}
	public void setClaimNum(int claimNum) {
		this.claimNum = claimNum;
	}
	
			
}
